export const BREEDS_BY_SPECIES: Record<string, string[]> = {
  'Dog': DOG_BREEDS,
  'Cat': CAT_BREEDS,
  'Bird': BIRD_TYPES,
  'Rabbit': RABBIT_BREEDS,
  'Other': OTHER_PETS,
};

export function getBreedsForSpecies(species: string): string[] {
  return BREEDS_BY_SPECIES[species] || OTHER_PETS;
}

export function searchBreeds(species: string, query: string): string[] {
  const breeds = getBreedsForSpecies(species);
  if (!query.trim()) return breeds.slice(0, 20);
  
  const lowerQuery = query.toLowerCase();
  const matches = breeds.filter(breed => 
    breed.toLowerCase().includes(lowerQuery)
  );
  
  // Sort by relevance - starts with query first
  return matches.sort((a, b) => {
    const aStarts = a.toLowerCase().startsWith(lowerQuery);
    const bStarts = b.toLowerCase().startsWith(lowerQuery);
    if (aStarts && !bStarts) return -1;
    if (!aStarts && bStarts) return 1;
    return a.localeCompare(b);
  }).slice(0, 20);
}
